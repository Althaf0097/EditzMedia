-- Storage Policies
-- We need to enable RLS on storage.objects
-- alter table storage.objects enable row level security;

create policy "Public Access"
  on storage.objects for select
  using ( bucket_id = 'assets' );

create policy "Admin Upload"
  on storage.objects for insert
  with check ( bucket_id = 'assets' and exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Admin Delete"
  on storage.objects for delete
  using ( bucket_id = 'assets' and exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

-- Trigger to create profile on signup
create or replace function public.handle_new_user()
returns trigger
language plpgsql
security definer set search_path = public
as $$
begin
  insert into public.profiles (id, email)
  values (new.id, new.email);
  return new;
end;
$$;

create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

